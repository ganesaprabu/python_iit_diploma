# In Java, we may not able to assign different data type value to the same variable.

# But in Python, same variable holds different data types like the below like int, float, string and list

#Just a comment
x = int(4)
print('As Int -> ', x)
x = float(4.7)
print('As Float -> ', x)
x = str('name')
print('As string -> ', x)
x = [10, 12, 67]
print('As list -> ', x)