# Logical operations

# Logical operator AND
print('# Logical operator AND [TRUE if all are true]')
print(True and True)
print(True and False)
print(False and True)
print(False and False)
print()

print('# Logical operator OR [TRUE if any one is true]')
# Logical operator OR
print(True or True)
print(True or False)
print(False or True)
print(False or False)
print()

print('# Logical operator NOT [inverse of]')
# Logical operator OR
print(not True)
print(not (True))

print()
print()
